// 1 - Write a for loop that runs 5 times
// Each iteration will increase the value
// of checkz by 1
var checkz = 0;
for(var i = 0; i < 5; i++){
  checkz++;
}
console.log(checkz);
// 2 - Write a while loop that runs
// 3 times. Each iteration will decrease
// the value of checkz by -2
i = 0;
while(i < 3) {
  checkz -= -2;
  i++;
}
console.log(checkz);
